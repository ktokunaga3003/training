// if文
class SampleIf{
	public static void main(String[] args) {
		int value = 10;

		//処理の分岐（偶数と奇数の判断）
		if(value % 2 == 0) {
			System.out.println("偶数");
		}else{
			System.out.println("奇数");
		}
	}
}